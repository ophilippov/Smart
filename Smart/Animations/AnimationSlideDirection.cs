

namespace Smart
{ 
    /// <summary>
    /// The direction an animation slides in (the slide out direction is reversed)
    /// <summary>
    public enum AnimationSlideDirection
    {
        /// <summary>
        /// Slide in from /out to the left
        /// </summary>
        Left = 0,
        /// <summary>
        /// Slide in from /out to the right
        /// </summary>
        Right = 1,
        /// <summary>
        /// Slide in from /out to the top
        /// </summary>
        Top = 2,
        /// <summary>
        /// Slide in from /out to the bottom
        /// </summary>
        Bottom = 3
    }
}
